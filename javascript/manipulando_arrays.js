/* declarando um array */
var dados = [1,2,3];

/* inserindo uma string no final do array */
dados.push('Deise');

/* remove o último item do array */
dados.pop();

/* inserindo uma string no inicio do array */
dados.unshift('Deise');

/* remove o primeiro item do array */
dados.shift();

/* ordena o array por ordem alfabética ou numeral */
dados.sort();

/* saber o tamanho do array */
dados.length;