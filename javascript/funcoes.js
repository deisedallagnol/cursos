/* declarando funções */
function fale(frase){
	alert(frase);
}

/* outra maneira de declarar funções */
 var fale = function(frase){
 	alert(frase);
 }

 /*usando (chamando a função) a função*/
 fale('Olá mundo!');

 /* os nomes das variáveis declaradas na funcção
não podem ter o mesmo nome dos parâmetros(argumentos, atributos).
 */